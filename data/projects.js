function displayItem(Item) {
    let displayCard = Item.map(function (item) {
        return `
        <div class="por__card" data-aos="${item.animation}">
            <div class="Cer_Ximg-box">
                <img src="${item.src}" alt="${item.alt}">
            </div>
            <div class="por__content-box">
                <div class="content">
                    <h2>${item.title}</h2>
                    <p>${item.paragraph}</p>
                    <button class="button-outline">
                        <a href="${item.demoLink}" class="button--link button--flex" target="_blank">Take A Look (Demo)
                            <i class="bx bx-right-arrow-alt"></i>
                        </a>
                    </button>
                    <button class="button">
                        <a href="${item.repoLink}" class="button--link button--flex" target="_blank">Code
                            <i class="bx bx-right-arrow-alt"></i>
                        </a>
                    </button>
                </div>
            </div>
        </div>
        `;
    })

    displayCard = displayCard.join('')
    cardsProject.innerHTML = displayCard;
}

